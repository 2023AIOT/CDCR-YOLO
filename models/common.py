class SMDDConv2d(nn.Conv2d):
    """
    SMDDConv / ODConv with switchable attention branches.

    Ablation switches:
        use_filter_attn:  filter-wise attention
        use_spatial_attn: spatial-wise attention
        use_channel_attn: channel-wise attention
        use_kernel_attn:  candidate-kernel attention
    """

    def __init__(
        self,
        in_channels,
        out_channels,
        kernel_size,
        stride=1,
        padding=0,
        dilation=1,
        groups=1,
        bias=True,
        K=4,
        r=1 / 16,
        save_parameters=False,
        use_filter_attn=True,
        use_spatial_attn=True,
        use_channel_attn=True,
        use_kernel_attn=True,
        padding_mode='zeros',
        device=None,
        dtype=None
    ):
        factory_kwargs = {'device': device, 'dtype': dtype}

        if K < 1:
            raise ValueError(f"K must be >= 1, but got {K}.")
        if in_channels % groups != 0:
            raise ValueError(
                f"in_channels={in_channels} must be divisible by groups={groups}."
            )
        if out_channels % groups != 0:
            raise ValueError(
                f"out_channels={out_channels} must be divisible by groups={groups}."
            )

        self.K = K
        self.r = r
        self.save_parameters = save_parameters

        self.use_filter_attn = use_filter_attn
        self.use_spatial_attn = use_spatial_attn
        self.use_channel_attn = use_channel_attn
        self.use_kernel_attn = use_kernel_attn

        super().__init__(
            in_channels,
            out_channels,
            kernel_size,
            stride,
            padding,
            dilation,
            groups,
            bias,
            padding_mode
        )

        # K candidate convolution kernels
        del self.weight
        self.weight = nn.Parameter(
            torch.empty(
                K,
                out_channels,
                in_channels // groups,
                *self.kernel_size,
                **factory_kwargs
            )
        )

        if bias:
            del self.bias
            self.bias = nn.Parameter(
                torch.empty(K, out_channels, **factory_kwargs)
            )

        hidden_dim = max(int(in_channels * r), 16)
        self.hidden_dim = hidden_dim

        self.gap = nn.AdaptiveAvgPool2d(1)
        self.fc = nn.Conv2d(in_channels, hidden_dim, 1, bias=False)
        self.bn = nn.BatchNorm2d(hidden_dim)
        self.act = nn.ReLU(inplace=True)

        # Create only the enabled attention branches.
        if self.use_filter_attn:
            self.fc_f = nn.Linear(hidden_dim, out_channels)

        spatial_is_meaningful = self.kernel_size[0] * self.kernel_size[1] > 1
        if self.use_spatial_attn and (
            not save_parameters or spatial_is_meaningful
        ):
            self.fc_s = nn.Linear(
                hidden_dim,
                self.kernel_size[0] * self.kernel_size[1]
            )

        channel_is_meaningful = in_channels // groups > 1
        if self.use_channel_attn and (
            not save_parameters or channel_is_meaningful
        ):
            self.fc_c = nn.Linear(
                hidden_dim,
                in_channels // groups
            )

        kernel_is_meaningful = K > 1
        if self.use_kernel_attn and (
            not save_parameters or kernel_is_meaningful
        ):
            self.fc_w = nn.Linear(hidden_dim, K)

        self.reset_parameters()

    def reset_parameters(self):
        fan_out = (
            self.kernel_size[0]
            * self.kernel_size[1]
            * self.out_channels
            // self.groups
        )

        for i in range(self.K):
            self.weight.data[i].normal_(0, math.sqrt(2.0 / fan_out))

        if self.bias is not None:
            self.bias.data.zero_()

        # Make the initial attention responses close to neutral.
        for name in ('fc_f', 'fc_s', 'fc_c', 'fc_w'):
            layer = getattr(self, name, None)
            if layer is not None:
                nn.init.normal_(layer.weight, std=0.001)
                if layer.bias is not None:
                    nn.init.zeros_(layer.bias)

    def extra_repr(self):
        return (
            super().extra_repr()
            + f", K={self.K}, r={self.r:.4f}"
            + f", filter_attn={self.use_filter_attn}"
            + f", spatial_attn={self.use_spatial_attn}"
            + f", channel_attn={self.use_channel_attn}"
            + f", kernel_attn={self.use_kernel_attn}"
        )

    def get_weight_bias(self, context):
        B, C, H, W = context.shape

        if C != self.in_channels:
            raise ValueError(
                f"Expected context with {self.in_channels} channels, "
                f"but got shape {list(context.shape)}."
            )

        # Shared embedding
        x = self.gap(context)
        x = self.fc(x)

        # BatchNorm fails for a single value per channel in training mode.
        if x.size(0) > 1 or not self.training:
            x = self.bn(x)

        x = self.act(x)
        x = x.flatten(1)  # B, hidden_dim

        # Start from a neutral broadcastable attention tensor.
        attn = x.new_ones((B, 1, 1, 1, 1, 1))

        # Filter attention: B, 1, C_out, 1, 1, 1
        if hasattr(self, 'fc_f'):
            attn_f = self.fc_f(x).sigmoid()
            attn = attn * attn_f.view(
                B, 1, self.out_channels, 1, 1, 1
            )

        # Spatial attention: B, 1, 1, 1, H_k, W_k
        if hasattr(self, 'fc_s'):
            attn_s = self.fc_s(x).sigmoid()
            attn = attn * attn_s.view(
                B, 1, 1, 1, *self.kernel_size
            )

        # Channel attention: B, 1, 1, C_in/G, 1, 1
        if hasattr(self, 'fc_c'):
            attn_c = self.fc_c(x).sigmoid()
            attn = attn * attn_c.view(
                B,
                1,
                1,
                self.in_channels // self.groups,
                1,
                1
            )

        # Kernel attention: B, K, 1, 1, 1, 1
        if hasattr(self, 'fc_w'):
            attn_w = self.fc_w(x).softmax(dim=-1)
        else:
            # Kernel-attention ablation:
            # aggregate candidate kernels with fixed uniform weights.
            attn_w = x.new_full(
                (B, self.K),
                fill_value=1.0 / self.K
            )

        attn = attn * attn_w.view(B, self.K, 1, 1, 1, 1)

        # W: K, C_out, C_in/G, H_k, W_k
        # Dynamic W: B, C_out, C_in/G, H_k, W_k
        weight = (attn * self.weight.unsqueeze(0)).sum(dim=1)

        weight = weight.reshape(
            B * self.out_channels,
            self.in_channels // self.groups,
            *self.kernel_size
        )

        bias = None
        if self.bias is not None:
            bias = attn_w @ self.bias
            bias = bias.reshape(-1)

        return weight, bias

    def forward(self, input, context=None):
        B, C, H, W = input.shape

        if C != self.in_channels:
            raise ValueError(
                f"Expected input with {self.in_channels} channels, "
                f"but got shape {list(input.shape)}."
            )

        # Do not use "context or input" because a Tensor cannot be
        # evaluated as a Boolean when context is explicitly provided.
        context_tensor = input if context is None else context
        weight, bias = self.get_weight_bias(context_tensor)

        output = nn.functional.conv2d(
            input.reshape(1, B * C, H, W),
            weight,
            bias,
            self.stride,
            self.padding,
            self.dilation,
            B * self.groups
        )

        output = output.reshape(
            B,
            self.out_channels,
            *output.shape[2:]
        )

        return output

    def debug(self, input, context=None):
        B, C, H, W = input.shape

        if C != self.in_channels:
            raise ValueError(
                f"Expected input with {self.in_channels} channels, "
                f"but got shape {list(input.shape)}."
            )

        context_tensor = input if context is None else context
        weight, bias = self.get_weight_bias(context_tensor)

        output_size = [
            (
                (H, W)[i]
                + 2 * self.padding[i]
                - self.dilation[i] * (self.kernel_size[i] - 1)
                - 1
            ) // self.stride[i] + 1
            for i in range(2)
        ]

        weight = weight.view(
            B,
            self.groups,
            self.out_channels // self.groups,
            -1
        )

        unfold = nn.functional.unfold(
            input,
            self.kernel_size,
            self.dilation,
            self.padding,
            self.stride
        )

        unfold = unfold.view(
            B,
            self.groups,
            -1,
            output_size[0] * output_size[1]
        )

        output = weight @ unfold
        output = output.view(
            B,
            self.out_channels,
            *output_size
        )

        if bias is not None:
            output = output + bias.view(
                B,
                self.out_channels,
                1,
                1
            )

        return output


class SMDDConv(nn.Module):
    """
    Structured Multi-Dimensional Dynamic Convolution.

    ablation:
        full
        no_filter
        no_spatial
        no_channel
        no_kernel
    """

    def __init__(
        self,
        c1,
        c2,
        k=1,
        s=1,
        kerNums=4,
        g=1,
        p=None,
        act=True,
        ablation="full"
    ):
        super().__init__()

        valid_modes = {
            "full",
            "no_filter",
            "no_spatial",
            "no_channel",
            "no_kernel"
        }

        if ablation not in valid_modes:
            raise ValueError(
                f"Unsupported ablation mode '{ablation}'. "
                f"Choose from {sorted(valid_modes)}."
            )

        self.ablation = ablation

        self.conv = SMDDConv2d(
            in_channels=c1,
            out_channels=c2,
            kernel_size=k,
            stride=s,
            padding=autopad(k, p),
            groups=g,
            K=kerNums,
            use_filter_attn=ablation != "no_filter",
            use_spatial_attn=ablation != "no_spatial",
            use_channel_attn=ablation != "no_channel",
            use_kernel_attn=ablation != "no_kernel"
        )

        self.bn = nn.BatchNorm2d(c2)
        self.act = (
            nn.SiLU()
            if act is True
            else act
            if isinstance(act, nn.Module)
            else nn.Identity()
        )

    def forward(self, x):
        return self.act(self.bn(self.conv(x)))

    def forward_fuse(self, x):
        return self.act(self.conv(x))

